package mypackage1;

public class ClaseTorneo 
{
  String torneo;
  String lugar;
  String gestion;

  public ClaseTorneo()
  {
  }

  public String getTorneo()
  {
    return torneo;
  }

  public void setTorneo(String newTorneo)
  {
    torneo = newTorneo;
  }

  public String getLugar()
  {
    return lugar;
  }

  public void setLugar(String newLugar)
  {
    lugar = newLugar;
  }

  public String getGestion()
  {
    return gestion;
  }

  public void setGestion(String newGestion)
  {
    gestion = newGestion;
  }
}